SELECT name_function(my_column) FROM my_table

-- COUNT возвращает количество записей
COUNT(my_column) 

-- SUM возвращает суму значений
SUM(my_column)

-- MIN возвращает минимальное значений из всех записей
MIN(my_column)

-- MAX возвращает максимально значений из всех записей
MAX(my_column)

-- AVG возвращает среднее ариметическое из всех значений указаной колонки
AVG(my_column) 
AVG(my_column2 - my_column1)